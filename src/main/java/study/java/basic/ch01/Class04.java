package study.java.basic.ch01;

public class Class04 {

    public static void main(String[] args) {
        //04.컴퓨터에서 자료 표현하기
        int num = 10;
        int bNum = 0b1010;
        int oNum = 012;
        int xNum = 0xA;

        System.out.println("num = " + num);
        System.out.println("bNum = " + bNum);
        System.out.println("oNum = " + oNum);
        System.out.println("xNum = " + xNum);
    }
}
